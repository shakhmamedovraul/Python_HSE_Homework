"""
Задание №1
Описание: Работа с переменными, ввод данных пользователем через input(),
вывод через print() и изучение адресов объектов в памяти через id().
"""

# Создаем переменные разных типов
legal_term = "Contract"  # строка (str) [7]
case_number = 40123  # целое число (int) [8]
is_active = True  # логический тип (bool) [8]

print("--- Базовые переменные ---")
print(f"Термин: {legal_term}, Номер дела: {case_number}, Статус: {is_active}")

# Запрашиваем данные у пользователя [2, 9]
user_name = input("Введите имя юриста: ")
user_experience = input("Введите стаж работы (лет): ")

print(f"Юрист {user_name} имеет стаж {user_experience} лет.")

# Изучаем id() объектов [2]
# id() возвращает адрес объекта в оперативной памяти.
# В Python числа и строки неизменяемы: если мы меняем значение, создается новый объект с новым id.
x = 100
print(f"ID числа 100: {id(x)}")
x = x + 1
print(f"ID числа 101 (после изменения x): {id(x)}")  # Адрес изменится

"""
Задание №2
Описание: Конвертация секунд в формат Часы:Минуты:Секунды 
с проверкой ввода через .isdigit().
"""

print("\n--- Конвертер времени ---")
user_seconds_str = input("Введите количество секунд для расчета: ")

# Проверка: ввел ли пользователь именно цифры [2, 10]
if user_seconds_str.isdigit():
    total_seconds = int(user_seconds_str)  # Приведение типа из str в int [11]

    # Математические расчеты [2, 12]
    hours = total_seconds // 3600  # Целочисленное деление для часов
    minutes = (total_seconds % 3600) // 60  # Остаток от часов делим на 60 для минут
    seconds = total_seconds % 60  # Остаток — это секунды

    print(f"Результат: {hours} ч. {minutes} мин. {seconds} сек.")
else:
    print("Ошибка: введите целое числовое значение (только цифры).")

"""
Задание №3
Описание: Расчет суммы n + nn + nnn для числа n от 1 до 9.
Пример: для 3 это 3 + 33 + 333 = 369.
"""

print("\n--- Расчет суммы n + nn + nnn ---")
n_str = input("Введите число n (от 1 до 9): ")

if n_str.isdigit() and 1 <= int(n_str) <= 9:
    # Используем особенность строк: в Python "3" * 2 даст "33" [3, 13]
    n1 = int(n_str)
    n2 = int(n_str + n_str)
    n3 = int(n_str + n_str + n_str)

    total_sum = n1 + n2 + n3
    print(f"Считаем: {n1} + {n2} + {n3} = {total_sum}")
else:
    print("Ошибка: введите одну цифру от 1 до 9.")
    