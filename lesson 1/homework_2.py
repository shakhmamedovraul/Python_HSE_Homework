"""
Задание №2 (дополнительное)
Описание: Реализация структуры данных «Участники спора».
Программа создает список словарей, где каждый словарь содержит
характеристики участника: наименование, статус и ИНН.
Данные вводятся пользователем через консоль.
"""

# Инициализируем пустой список для хранения участников
participants = []

print("--- Регистрация участников спора ---")

# Реализуем ввод данных для трех участников через цикл
for i in range(1, 4):
    print(f"\nВведите данные для участника №{i}:")

    # Запрашиваем информацию у пользователя
    name = input("Наименование (ФИО или название организации): ")
    status = input("Процессуальный статус (Истец, Ответчик, Третье лицо): ")
    inn = input("ИНН: ")

    # Создаем словарь для текущего участника
    participant_dict = {
        "name": name,
        "status": status,
        "inn": inn
    }

    # Добавляем сформированный словарь в общий список
    participants.append(participant_dict)

# Выводим полученную структуру данных в консоль
print("\n--- Итоговый реестр участников ---")
print(participants)